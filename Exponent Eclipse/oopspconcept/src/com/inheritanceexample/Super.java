package com.inheritanceexample;

public class Super {

	int x = 20;

	public void m1() {

		System.out.println("m1 method of parent class");

	}

	public void m3() {

		System.out.println("m3 method of parent class");
	}
	public void m4() {
		System.out.println("m4 method of parent class not overrided");
	}
}
