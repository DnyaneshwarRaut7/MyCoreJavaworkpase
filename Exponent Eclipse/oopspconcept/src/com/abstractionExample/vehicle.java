package com.abstractionExample;

public abstract class vehicle {

	public void tyre() {    // non abstract method

		System.out.println("=== 4 wheels ===");
	}

	public void Tering() {          // non abstract method

		System.out.println(" ==== 1 tering ===== ");
	}

	public abstract void specification();  // abstract method
}

// abstraction of abstract class  