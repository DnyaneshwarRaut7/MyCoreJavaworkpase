public class Pattreanlogic {

	public static void main(String[] args) {

		for (int i = 0; i <= 5; i++) {

			for (int j = 1; j <= i; j++) {

				System.out.print(j);
			}
			System.out.println();
		}

		
		
		for (int k = 0;k <=5;k++) {
			
			for (int l = 1;l<=k;l++) {
				
				
				System.out.print("*");
				}
				System.out.println();
			}
			
			
			
		} 
	
}
